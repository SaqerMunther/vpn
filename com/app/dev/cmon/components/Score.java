package com.app.dev.cmon.components;

public class Score {
	private String location;
	private int totalComply;
	private double totalNotComply;
	
	
	
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public int getTotalComply() {
		return totalComply;
	}
	public void setTotalComply(int totalComply) {
		this.totalComply = totalComply;
	}
	public double getTotalNotComply() {
		return totalNotComply;
	}
	public void setTotalNotComply(double totalNotComply) {
		this.totalNotComply = totalNotComply;
	}
	
	
	
}
